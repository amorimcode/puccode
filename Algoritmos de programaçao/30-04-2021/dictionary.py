# estrutura para armazenar coleções de itens
# usados {} para seus itens, separados por vírgulas
# 
# ex

tabela_preço = {'cadeira': 250.56, 'mesa': 1234.00, 'estante': 2500}

# incluindo item no dicionário
tabela_preço['sofa'] = 3500
print(tabela_preço)

# excluindo um item pela chave
del tabela_preço['estante']
print(tabela_preço)

